$("#btnSubmitConsultation").click(function(){
    var error = 0;
    var errorArr = [];
    
    $(".requiredConsultationField").each(function(){
        if($.trim($(this).val())==''||$.trim($(this).val())==null){
            $(this).css('border-color','#ff0000');
            error = 1;
            errorArr.push(error);
        }else{
            $(this).css('border-color','');
            error = 0;
            errorArr.push(error);
        }
    });
    
    var txtContactNoConsultation = $("#txtContactNoConsultation").val();
    if(txtContactNoConsultation.length !== 10){
        $("#txtContactNoConsultation").css('border-color','#ff0000');
        error = 1;
        errorArr.push(error);
    }else{
        $("#txtContactNoConsultation").css('border-color','');
        error = 0;
        errorArr.push(error);
    }
    
    var email = $('#txtEmailConsultation').val();
    var batposition = email.indexOf("@");  
    var bdotposition = email.lastIndexOf(".");  
    if(batposition<1 || bdotposition<batposition+2 || bdotposition+2>=email.length){  
        $('#txtEmailConsultation').css('border-color','#ff0000');
        error = 1;
        errorArr.push(error);
    }else{
        $('#txtEmailContactUs').css('border-color','');
        error = 0;
        errorArr.push(error);
    }
    
    
    if(errorArr.indexOf(1) !== -1){ 
      return false;
    }else{
      if (grecaptcha.getResponse()){    
          
          $("#ConsultationSpinner").css('display','inline-block');
          var form=$("#consultationForm");
          $.ajax({
            type:"POST",
            url: window.location.origin +'/consultationSubmit', 
            data:form.serialize(), 
            success:function(msg){
                if(msg=='success'){
                    $("#ConsultationSpinner").css('display','none');
                    // $(".consultationSuccessMessage").css('display','block');
                    // $('#feedBackModal').modal('show');
                    window.location.href = "https://asesasoft.com/thank-you";
                    grecaptcha.reset();
                    setTimeout(function() {
                        $(".consultationSuccessMessage").css('display','none');
                        $("#txtFirstNameConsultation").val('');
                        $("#txtLastNameConsultation").val('');
                        $("#txtContactNoConsultation").val('');
                        $("#txtEmailConsultation").val('');
                        $("#txtMessageConsultation").val('');
                    }, 5000);
                }else{
                    alert('Something went wrong.');
                }
            }
          });
      }else{
     	alert('Please confirm captcha to proceed');
      }
    }
});    


